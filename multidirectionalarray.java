public class multidirectionalarray {
    public static void main(String[] args) {
        int marks[][] = new int[4][3];

        marks[0][0] = 12;
        marks[0][1] = 98;
        marks[0][2] = 34;

        marks[1][0] = 12;
        marks[1][1] = 67;
        marks[1][2] = 87;
        System.out.println(marks[1][0]);
        System.out.println(marks[1][1]);

    }
}
